package Zajecia1;

public class ZmienneDzialania {
    public static void main(String[] args) {
        int a = 223;
        int b = 10;
        System.out.println("Wartość a to "+a);
        System.out.println("Wartość b to "+b);

        System.out.println("wynik dodawania a+b to "+(a+b));

        System.out.println("Wynik odejmowania a-b to "+(a-b));
        System.out.println("Wynik mnożenia a*b to "+(a*b));
        System.out.println("wynik dzielenia a/b to "+(a/b));
        System.out.println("Reszta z dzielenia a/b to "+(a%b));




    }
}
